package model;

public class Dados {
	private int dado1;
	private int dado2;
	private int soma;
	
	public Dados() {
		this.dado1 = 0;
		this.dado2 = 0;
		this.soma = 0;
	}

	public int getDado1() {
		return dado1;
	}

	public void setDado1(int dado1) {
		this.dado1 = dado1;
	}

	public int getDado2() {
		return dado2;
	}

	public void setDado2(int dado2) {
		this.dado2 = dado2;
	}

	public int getSoma() {
		return soma;
	}

	public void setSoma(int soma) {
		this.soma = soma;
	}
	
	
}
